#!/bin/sh

# When modifying a schema to add a NEW $ref to another schema that it
# did not previously reference, the newly referenced schema's dependents
# should rebuild. Unrelated schemas' dependents must not be scheduled.

set -o errexit
set -o nounset

TMP="$(mktemp -d)"
clean() { rm -rf "$TMP"; }
trap clean EXIT

cat << EOF > "$TMP/one.json"
{
  "url": "https://sourcemeta.com/",
  "contents": {
    "example": {
      "contents": {
        "schemas": {
          "baseUri": "https://example.com/",
          "path": "./schemas"
        }
      }
    }
  }
}
EOF

mkdir "$TMP/schemas"

cat << 'EOF' > "$TMP/schemas/a.json"
{
  "$schema": "http://json-schema.org/draft-07/schema#",
  "$id": "https://example.com/a"
}
EOF

# B has no references initially
cat << 'EOF' > "$TMP/schemas/b.json"
{
  "$schema": "http://json-schema.org/draft-07/schema#",
  "$id": "https://example.com/b"
}
EOF

cat << 'EOF' > "$TMP/schemas/c.json"
{
  "$schema": "http://json-schema.org/draft-07/schema#",
  "$id": "https://example.com/c"
}
EOF

remove_threads_information() {
  expr='s/ \[[^]]*[^a-z-][^]]*\]//g'
  if [ "$(uname -s)" = "Darwin" ]; then
    sed -i '' "$expr" "$1"
  else
    sed -i "$expr" "$1"
  fi
}

# Run 1: full build from scratch
"$1" --skip-banner "$TMP/one.json" "$TMP/output" \
    > /dev/null 2>&1

# Run 2: modify B to add a $ref to A
cat << 'EOF' > "$TMP/schemas/b.json"
{
  "$schema": "http://json-schema.org/draft-07/schema#",
  "$id": "https://example.com/b",
  "properties": {
    "foo": { "$ref": "https://example.com/a" }
  }
}
EOF
"$1" --skip-banner "$TMP/one.json" "$TMP/output" \
    2> "$TMP/output.txt"
remove_threads_information "$TMP/output.txt"
grep -E "Producing|Combining" "$TMP/output.txt" > "$TMP/output_producing.txt"

# A's dependents must rebuild (B now references A)
grep -q "schemas/example/schemas/a/%/dependents.metapack" \
    "$TMP/output_producing.txt"

# B's dependents must NOT rebuild (nobody references B)
grep -q "schemas/example/schemas/b/%/dependents.metapack" \
    "$TMP/output_producing.txt" && exit 1

# C's dependents must NOT rebuild (C is unrelated)
grep -q "schemas/example/schemas/c/%/dependents.metapack" \
    "$TMP/output_producing.txt" && exit 1

# Verify no files were deleted
cd "$TMP/output"
find . -mindepth 1 | LC_ALL=C sort > "$TMP/manifest.txt"
